-- turn on line numbers
vim.opt.nu = true

-- tab settings
vim.opt.tabstop = 4
vim.opt.softtabstop = 4
vim.opt.shiftwidth = 4
vim.opt.expandtab = true
vim.opt.smartindent = true

-- no line wrap
vim.opt.wrap = false

-- update search as you are typing
vim.opt.incsearch = true

-- keep at least 8 lines at edge of scroll
vim.opt.scrolloff = 8

-- Always draw the signcolumn
vim.opt.signcolumn = "yes"

-- decrease the waiting time for writing .swap file to disk
vim.opt.updatetime = 1000

-- draw color column at character limit
vim.opt.colorcolumn = "100"
